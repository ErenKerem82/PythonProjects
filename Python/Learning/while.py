while True:

    sayi = "1"
    sayi1 = "2"
    sayi2 = "3"
    sayi3 = "4"
    sayi4 = "5"
    sayisor = input("1-5 arası bir sayı gir : ")

    if sayisor == sayi:
        print("   1 girdiniz  ")

    elif sayisor == sayi1:
        print("   2 girdiniz")

    elif sayisor == sayi2:
        print("   3 girdiniz")

    elif sayisor == sayi3:
        print("   4 girdiniz")

    elif sayisor == sayi4:
        print("   5 girdiniz")

    else:
        print("lütfen 1-5 arası bir sayı giriniz")

    continue
